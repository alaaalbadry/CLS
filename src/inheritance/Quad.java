package inheritance;

public class Quad extends Shape {

    public	double x;
	public double y;
	
	public Quad() {
		System.out.println("created a quad");
	}
	
	 
	
	
	public double getX() {
		return x;
	}



	public void setX(double x) {
		this.x = x;
	}



	public double getY() {
		return y;
	}



	public void setY(double y) {
		this.y = y;
	}




	
	
}
